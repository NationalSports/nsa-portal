#!/usr/bin/env node
/**
 * Generates an idempotent SQL script that loads the Agron product catalog
 * into Supabase. Output: data/agron-import.sql
 *
 * Usage: node scripts/build-agron-sql.js
 *
 * The output script:
 *   1. Locates (or inserts) the "Agron" vendor row.
 *   2. Upserts every product row from data/agron-import.csv into `products`
 *      keyed by a deterministic id ('p-agron-' + sku) so re-runs only patch
 *      changed fields.
 *   3. Reports the final count.
 */

const fs = require('fs');
const path = require('path');

const SRC = path.resolve(__dirname, '..', 'data', 'agron-import.csv');
const OUT = path.resolve(__dirname, '..', 'data', 'agron-import.sql');

const parseRow = (line) => {
  const out = [];
  let cur = '';
  let inQ = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (inQ) {
      if (c === '"' && line[i + 1] === '"') { cur += '"'; i++; }
      else if (c === '"') inQ = false;
      else cur += c;
    } else {
      if (c === '"') inQ = true;
      else if (c === ',') { out.push(cur); cur = ''; }
      else cur += c;
    }
  }
  out.push(cur);
  return out;
};

const sqlStr = (s) => {
  if (s == null || s === '') return 'NULL';
  return `'${String(s).replace(/'/g, "''")}'`;
};
const sqlNum = (s) => {
  const n = parseFloat(s);
  return Number.isFinite(n) ? String(n) : 'NULL';
};
const sqlSizes = (s) => {
  if (!s) return `'[]'::jsonb`;
  const arr = String(s).split(',').map((x) => x.trim()).filter(Boolean);
  return `'${JSON.stringify(arr).replace(/'/g, "''")}'::jsonb`;
};

const lines = fs.readFileSync(SRC, 'utf-8').split(/\r?\n/).filter(Boolean);
const header = parseRow(lines.shift()).map((h) => h.trim());
const rows = lines.map((line) => {
  const cells = parseRow(line);
  const r = {};
  header.forEach((h, i) => { r[h] = (cells[i] || '').trim(); });
  return r;
});

const valuesSql = rows.map((r) => {
  const id = `p-agron-${r.sku}`;
  return `  (${sqlStr(id)}, v_id, ${sqlStr(r.sku)}, ${sqlStr(r.name)}, ${sqlStr(r.brand)}, ${sqlStr(r.color)}, ${sqlStr(r.category)}, ${sqlNum(r.retail_price)}, ${sqlNum(r.nsa_cost)}, ${sqlSizes(r.available_sizes)}, true)`;
}).join(',\n');

const sql = `-- Agron FW26 product catalog loader
-- Generated by scripts/build-agron-sql.js
-- Idempotent: re-running only patches changed columns. Safe to run multiple
-- times. Run inside the Supabase SQL Editor (Production project, role=postgres).
DO $$
DECLARE
  v_id TEXT;
BEGIN
  -- Look up the Agron vendor (case-insensitive); create it if missing.
  SELECT id INTO v_id FROM public.vendors WHERE lower(name) = 'agron' LIMIT 1;
  IF v_id IS NULL THEN
    v_id := 'v-agron-' || extract(epoch from now())::bigint;
    INSERT INTO public.vendors (id, name, vendor_type, payment_terms, is_active)
    VALUES (v_id, 'Agron', 'upload', 'net30', true);
    RAISE NOTICE 'Created Agron vendor: %', v_id;
  ELSE
    RAISE NOTICE 'Found Agron vendor: %', v_id;
  END IF;

  -- Upsert ${rows.length} products. id = 'p-agron-' || sku makes re-runs
  -- patch the same row instead of inserting duplicates.
  INSERT INTO public.products
    (id, vendor_id, sku, name, brand, color, category,
     retail_price, nsa_cost, available_sizes, is_active)
  VALUES
${valuesSql}
  ON CONFLICT (id) DO UPDATE SET
    vendor_id       = EXCLUDED.vendor_id,
    name            = EXCLUDED.name,
    brand           = EXCLUDED.brand,
    color           = EXCLUDED.color,
    category        = EXCLUDED.category,
    retail_price    = EXCLUDED.retail_price,
    nsa_cost        = EXCLUDED.nsa_cost,
    available_sizes = EXCLUDED.available_sizes,
    is_active       = EXCLUDED.is_active,
    updated_at      = now();

  RAISE NOTICE 'Agron products loaded. Total in DB now: %',
    (SELECT count(*) FROM public.products WHERE vendor_id = v_id);
END $$;
`;

fs.writeFileSync(OUT, sql);
console.log(`Wrote ${rows.length} product upserts to ${OUT}`);
console.log(`SQL size: ${(fs.statSync(OUT).size / 1024).toFixed(1)} KB`);
